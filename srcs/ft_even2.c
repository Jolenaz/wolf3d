#include "wolf3d.h"

static void	ft_remap(t_env *e)
{
	if (e->qr == 3)
	{
		e->map[15][8] = 0;
		e->map3[15][9] = -3;
	}
	else if (e->qr == 4)
	{
		e->map[7][15] = 0;
		e->map3[7][14] = -4;
	}
	else if (e->qr == 5)
	{
		e->map[1][13] = 0;
		e->map3[1][12] = -5;
	}
}

void		ft_open_door(t_env *e)
{
	static int i = 0;

	if (i < 10)
	{
		e->dd += 20;
		i++;
	}
	else
	{
		i = 0;
		e->key49 = 0;
		e->dd = 0;
		ft_remap(e);
	}
}
